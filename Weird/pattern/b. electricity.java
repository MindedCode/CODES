public class Main
{
	public static void main(String[] args) {
	    int n = 11;
	    for(int i=1; i<n; i++){
	        for(int j=i; j<n; j++){
	            for(int k=j; k<n; k++){
	                System.out.print("\u26a1");
	            }
	            System.out.println();
	        }
	       //System.out.print("\t");
	    }
		System.out.println("Hello World");
	}
}


